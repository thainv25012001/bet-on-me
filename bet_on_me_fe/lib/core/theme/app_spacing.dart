/// Nike 8px spacing grid tokens.
///
/// All spacing in the app should snap to these values.
abstract final class AppSpacing {
  /// 4px — tight icon gaps, inline spacing.
  static const double s1 = 4;

  /// 8px — base unit, button icon gaps.
  static const double s2 = 8;

  /// 12px — card internal padding, tight margins.
  static const double s3 = 12;

  /// 16px — standard padding, nav spacing.
  static const double s4 = 16;

  /// 20px — product card gaps.
  static const double s5 = 20;

  /// 24px — section internal padding, grid gaps.
  static const double s6 = 24;

  /// 32px — section breaks.
  static const double s7 = 32;

  /// 48px — major section padding.
  static const double s8 = 48;

  /// 64px — hero section padding.
  static const double s9 = 64;

  /// 80px — large section spacing.
  static const double s10 = 80;
}
